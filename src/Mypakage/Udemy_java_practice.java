package Mypakage;

public class Udemy_java_practice {

	public static void main(String[] args) {
		
		int k=1;
		for(int i=1; i<=3; i++) {
			for(int j=1;j<=i; j++) {
				System.out.print((k*3)+" ");
				k++;
				
			}
			System.out.println("");
		}
		
		for(int p=1; p<50; p=p+2) {
			System.out.println(p);
		}
		
		

	}

}
